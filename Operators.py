# Example 2: Operators
# Arithmetic Operators
a = 10
b = 5
print("Addition:", a + b)     # 10 + 5 = 15
print("Subtraction:", a - b)  # 10 - 5 = 5
print("Multiplication:", a * b)  # 10 * 5 = 50
print("Division:", a / b)     # 10 / 5 = 2.0
print("Floor Division:", a // b)  # 10 // 5 = 2
print("Modulus:", a % b)      # 10 % 5 = 0
print("Exponentiation:", a ** b)  # 10 ** 5 = 100000

# Comparison Operators
x = 10
y = 20
print("Is x equal to y?", x == y)  # False
print("Is x not equal to y?", x != y)  # True
print("Is x less than y?", x < y)   # True
print("Is x greater than or equal to y?", x >= y)  # False

# Logical Operators
p = True
q = False
print("p and q:", p and q)    # False
print("p or q:", p or q)      # True
print("not p:", not p)        # False
